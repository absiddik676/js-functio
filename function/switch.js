const color = "yellow";
switch (color) {
    case "green":
        console.log("you are my green friend");
        break;
    case "red":
        console.log('you are my red friend');
        break;
        case "yellow":
            console.log('you are my yellow friend');
            break;
        case "white":
            console.log('you are my white friend');
            break;
    default:
        console.log('you are my black black friend');
        break;
}